# # creating a list 

myfruitlist = ["apple", "banana", "cherry"]
print(myfruitlist)
print(type(myfruitlist))

# access list by position

print(myfruitlist[0])
print(myfruitlist[1])
print(myfruitlist[2])

# Changing the values in a list

myfruitlist[2] = "orange"

print(myfruitlist[2])

print(myfruitlist)

# creating a tuple

myfinalanswertuple = ("apple", "banana", "pineapple")
print(myfinalanswertuple)
print(type(myfinalanswertuple))

# access tuple by position

print(myfinalanswertuple[0])
print(myfinalanswertuple[1])
print(myfinalanswertuple[2])

# using dictionary data type

myfavouritefruitdictionary = {
    "Akua" : "apple",
  "Saanvi" : "banana",
  "Paulo" : "pineapple"
}
print(myfavouritefruitdictionary)
print(type(myfavouritefruitdictionary))

# Accessing a dictionary by name

print(myfavouritefruitdictionary["Akua"])
print(myfavouritefruitdictionary["Saanvi"])
print(myfavouritefruitdictionary["Paulo"])

